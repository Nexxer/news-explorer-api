const mongoose = require('mongoose');

// Все поля схемы обязательны, image и link проверяются на соответствие ссылке
const articleSchema = new mongoose.Schema({
  keyword: {
    type: String,
    required: true,
  },
  title: {
    type: String,
    required: true,
  },
  text: {
    type: String,
    required: true,
  },
  date: {
    type: String,
    required: true,
  },
  source: {
    type: String,
    required: true,
  },
  link: {
    type: String,
    required: true,
  },
  image: {
    type: String,
    required: true,
  },
  owner: {
    type: String,
    select: false,
  },
});

module.exports = mongoose.model('article', articleSchema);
